<template>
  <div class="row">
    <div class="col-lg-4">

    </div>
    <div class="col-lg-4">
      <div class="nb-login">
        <h3 class="scenter">Sign In</h3>
        <form @submit.prevent="login({username, password})">
          <div class="form-group">
            <input type="text" class="form-control" v-model="username" placeholder="Enter Username" required>
          </div>
          <div class="form-group">
            <input type="password" class="form-control" v-model="password" placeholder="Enter Password" required>
          </div>
          <div class="checkbox">
            <label>
              <input type="checkbox"> Remember Me
        </label>
          </div>
          <button type="submit" class="btn btn-block">Sign In</button>
        </form>
      </div>
    </div>
  </div>
</template>
<script>
  export default {
    data() {
      return {
        username: '',
        password: ''
      };
    },
    methods: {
      login() {
        this.$store.dispatch('LOGIN', {
          username: this.username,
          password: this.password
        }).then(() => {
          this.$router.push({ name: 'dashboard' });
        })
          .catch((error) => {
            console.log(error);
          });
      }
    }
  };
</script>
